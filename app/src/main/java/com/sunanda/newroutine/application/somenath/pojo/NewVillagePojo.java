package com.sunanda.newroutine.application.somenath.pojo;

public class NewVillagePojo  {

    private String villageName;
    private String VillageCode;
    private String villageId;
    private String totalHab;
    private String testedHab;

    public String getVillageName() {
        return villageName;
    }

    public void setVillageName(String villageName) {
        this.villageName = villageName;
    }

    public String getVillageCode() {
        return VillageCode;
    }

    public void setVillageCode(String villageCode) {
        VillageCode = villageCode;
    }

    public String getVillageId() {
        return villageId;
    }

    public void setVillageId(String villageId) {
        this.villageId = villageId;
    }

    public String getTotalHab() {
        return totalHab;
    }

    public void setTotalHab(String totalHab) {
        this.totalHab = totalHab;
    }

    public String getTestedHab() {
        return testedHab;
    }

    public void setTestedHab(String testedHab) {
        this.testedHab = testedHab;
    }
}
